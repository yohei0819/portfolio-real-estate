// --------------------------------------------------
// シードから自動生成した物件をマージ
// 開始IDを既存最大ID+1で自動算出し、手書きID追加時の衝突を防止
// --------------------------------------------------
const START_ID = Math.max(...Object.keys(PROPERTIES).map(Number)) + 1
const GENERATED = expandSeeds(SEEDS, START_ID)
for (const [id, prop] of Object.entries(GENERATED)) {
  PROPERTIES[id] = prop
}

// 類似物件IDを全物件に対して自動割り当て
assignSimilarIds(PROPERTIES)

// --------------------------------------------------
// 初期費用合計を自動計算（手動入力ミスを防止）
// --------------------------------------------------
for (const prop of Object.values(PROPERTIES)) {
  const costs = prop.initialCosts
  if (!costs) continue
  costs.total = COST_KEYS.reduce(
    (sum, key) => sum + (costs[key]?.amount ?? 0), 0,
  )
}

